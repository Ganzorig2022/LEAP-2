const PI = 3.14
const sqrt = 2

function round(input) {
  return Math.round(input)
}

function abs(input) {
  return Math.abs(input)
}

function floor(input) {
  return Math.floor(input)
}

function max(input) {
  return Math.max(...input)
}


exports.PI = PI
exports.sqrt = sqrt
exports.round = round
exports.abs = abs
exports.floor = floor
exports.max = max
